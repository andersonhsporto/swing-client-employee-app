    public void list() {
        VendorDAO vendorDAO = new VendorDAO();
        List<VendorEntity> list = vendorDAO.findAllVendor();
        DefaultTableModel data = (DefaultTableModel) tableVendor.getModel();

        data.setNumRows(0);

        for (VendorEntity c : list) {
            data.addRow(new Object[]{
                c.getId(),
                c.getName(),
                c.getCnpj(),
                c.getEmail(),
                c.getTelephone(),
                c.getMobile(),
                c.getCEP(),
                c.getAddress(),
                c.getNumber(),
                c.getAddressComplement(),
                c.getNeighborhood(),
                c.getCity(),
                c.getState()
            });
        }

    }

